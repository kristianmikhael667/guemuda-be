    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $validatedData = $request->validate([
            'name' => 'required',
        ]);
        $slug = SlugService::createSlug(ModelsCategoryCommunity::class, 'slug', $request->name);
        $validatedData['slug'] = $slug;
        ModelsCategoryCommunity::create($validatedData);
        return redirect('/administrator/category-community')->with('success', 'New Category has been added!');
    }

